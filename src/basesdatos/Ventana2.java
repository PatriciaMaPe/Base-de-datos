    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //BOTON INSERTAR
        Conector conector = new Conector("127.0.0.1", usuario, contrasenha, "usuarios");
        try {
            Connection conexion = (Connection) conector.getConexion();
            Statement statement = conexion.createStatement();

            String nombre = jTextField1.getText();
            String apellidos = jTextField2.getText();
            String dni = jTextField3.getText();
            String contrasenha = jPasswordField1.getText();
            if (nombre.equals("") || apellidos.equals("") || dni.equals("") || contrasenha.equals("")) {
                JOptionPane.showMessageDialog(this, "Se deben rellenar todos los campos");
            } else {
                statement.execute("insert into usuarios values('" + nombre + "','"
                        + apellidos + "','" + dni + "','" + contrasenha + "')");
                JOptionPane.showMessageDialog(this, "Datos ingresados correctamente");
            }
            jTextField1.setText(null);
            jTextField2.setText(null);
            jTextField3.setText(null);
            jPasswordField1.setText(null);

            statement.close();
            conexion.close();
            conector.cerrarConexion();

        } catch (Exception ex) {
            System.out.println("error");
            ex.getMessage();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //BOTON SELECT
        Conector conector = new Conector("127.0.0.1", usuario, contrasenha, "usuarios");
        ConsultasSQL consulta = new ConsultasSQL(conector.getConexion());

        try {           
            if (jTextArea1.getText().substring(0, 6).equalsIgnoreCase("select")
                    || jTextArea1.getText().substring(0, 6).equalsIgnoreCase("descri")) {
                VentanaTabla ventanatabla = new VentanaTabla();
                consulta.consultaSelect(jTextArea1.getText());
                ventanatabla.crearVentana(consulta.getDatosDevueltos(), consulta.getNombresColumnas());
            } else {
                consulta.consultar(jTextArea1.getText());
                JOptionPane.showMessageDialog(this, "Consulta realizada");
            }
            conector.cerrarConexion();
        } catch (Exception ex) {
            System.out.println("error");
            JOptionPane.showMessageDialog(this, "Introduce datos");

        }
    }//GEN-LAST:event_jButton2ActionPerformed


